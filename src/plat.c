#include "fdf.h"

void	plat(t_info *info)
{
	info->anglez = 0;
	info->anglex = 0;
}

void	iso(t_info *info)
{
	info->anglez = M_PI / 4;
	info->anglex = M_PI / 2 - atan(M_PI / 6);
}
